export function iconSvg(name, { size = 18, stroke = 1.5, cls = "" } = {}) {
  const ds = PATHS[name];
  if (!ds) return "";
  const body = ds.map((d) => `<path d="${d}"></path>`).join("");
  return (
    `<svg class="icon${cls ? " " + cls : ""}" viewBox="0 0 24 24" width="${size}" height="${size}" ` +
    `fill="none" stroke="currentColor" stroke-width="${stroke}" stroke-linecap="round" ` +
    `stroke-linejoin="round" stroke-miterlimit="10" aria-hidden="true" focusable="false">${body}</svg>`
  );
}

export function setIcon(el, name, opts) {
  if (el) el.innerHTML = iconSvg(name, opts);
}

/** Replace the contents of every `[data-icon]` element with its icon. */
export function mountIcons(root = document) {
  for (const el of root.querySelectorAll("[data-icon]")) {
    const size = el.dataset.iconSize ? Number(el.dataset.iconSize) : 18;
    el.innerHTML = iconSvg(el.dataset.icon, { size });
  }
}
